%%--------------------------- pp4
goodplot();
A = csvread('aat-pp4.csv');
plot(A(:,1), A(:,5), '*-', 'LineWidth', 4,'markers',14,'Color', [20, 200, 20]/256);
hold on; grid on;
plot(A(:,1), A(:,6), 's-', 'LineWidth', 4,'markers',14,'Color', [20, 20, 200]/256);
text(1.2,A(1,6),strcat('\leftarrow',num2str(100*A(1,8)),'% non-zero cells'),'FontSize', 16,'FontWeight','Bold');
plot(A(:,1), A(:,7), '^-', 'LineWidth', 4,'markers',14,'Color', [200, 20, 20]/256);
legend('Lower Bound', 'Actual Size', 'Upper Bound','location','east');
ylabel('No. of cells \geq \tau in AA^T');
xlabel('threshold (\tau)');
axis([1 9 7*10^4 10^8]);
%set(gca,'XTick',1:1:7);
%set(gca,'xticklabels',{'N1','N2','N3','N4','N5','N6','N7'});
%set(gca,'XTickLabelRotation',45)
set(gca,'FontSize',16);
set(gca,'FontWeight','Bold');
set(get(gca,'xlabel'),'FontSize', 16, 'FontWeight', 'Bold');
set(get(gca,'ylabel'),'FontSize', 16, 'FontWeight', 'Bold');
set(gca,'YScale','log');
%xlabel('');
%set(gca,'YTick',cellstr(num2str(round(log10(YTick(:))), '10^%d')));
print -dpdf -r150 aat-pp4.pdf;
hold off;
