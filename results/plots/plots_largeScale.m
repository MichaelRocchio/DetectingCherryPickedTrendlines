goodplot();
A = csvread('20k1.csv');
plot(A(:,2), A(:,4), 's-', 'LineWidth', 4,'markers',14,'Color', [200, 20, 20]/256);
hold on; grid on;
plot(A(:,2), A(:,3), 's-', 'LineWidth', 4,'markers',14,'Color', [20, 200, 20]/256);
legend('Direct-E','Direct-A','location','northwest');
ylabel('time (sec)');
xlabel('number of columns (m)');
%axis([1 7 0.005 20]);
%set(gca,'XTick',1:1:7);
%set(gca,'xticklabels',{'N1','N2','N3','N4','N5','N6','N7'});
%set(gca,'XTickLabelRotation',45)
set(gca,'FontSize',16);
set(gca,'FontWeight','Bold');
set(get(gca,'xlabel'),'FontSize', 16, 'FontWeight', 'Bold');
set(get(gca,'ylabel'),'FontSize', 16, 'FontWeight', 'Bold');
%set(gca,'YScale','log');
%xlabel('');
%set(gca,'YTick',cellstr(num2str(round(log10(YTick(:))), '10^%d')));
print -dpdf -r150 S_20k1.pdf;
hold off;
